async function translateAreaDescriptions() {
  try {
    console.log('üîÑ Connecting to database...');
    await AppDataSource.initialize();
    console.log('‚úÖ Database connected\n');

    const areaRepository = AppDataSource.getRepository(Area);
    const queryRunner = AppDataSource.createQueryRunner();
    await queryRunner.connect();

    try {
      // –û—Ç—Ä–∏–º—É—î–º–æ –≤—Å—ñ —Ä–∞–π–æ–Ω–∏
      const areas = await areaRepository.find({
        order: { nameEn: 'ASC' },
      });

      console.log(`üìñ Found ${areas.length} areas to process\n`);

      let updatedCount = 0;
      let skippedCount = 0;
      const notFound: string[] = [];

      for (const area of areas) {
        const translation = areaTranslations[area.nameEn];

        if (!translation) {
          console.log(`‚ö†Ô∏è  No translation found for: ${area.nameEn}`);
          notFound.push(area.nameEn);
          skippedCount++;
          continue;
        }

        // –û—Ç—Ä–∏–º—É—î–º–æ –ø–æ—Ç–æ—á–Ω–∏–π description –∑ –±–∞–∑–∏ —á–µ—Ä–µ–∑ raw query
        const areaRaw = await queryRunner.query(
          `SELECT description, infrastructure FROM areas WHERE id = $1`,
          [area.id]
        );

        const currentDescription = areaRaw[0]?.description || null;
        const currentInfrastructure = areaRaw[0]?.infrastructure || null;

        // –°—Ç–≤–æ—Ä—é—î–º–æ –Ω–æ–≤—É —Å—Ç—Ä—É–∫—Ç—É—Ä—É –∑ en —Ç–∞ ru –≤–µ—Ä—Å—ñ—è–º–∏
        let newDescription: any = {
          en: {},
          ru: {},
        };

        // –Ø–∫—â–æ —î —Å—Ç–∞—Ä–∏–π —Ñ–æ—Ä–º–∞—Ç (backward compatibility)
        if (currentDescription && typeof currentDescription === 'object') {
          if (currentDescription.title || currentDescription.description) {
            // –°—Ç–∞—Ä–∏–π —Ñ–æ—Ä–º–∞—Ç - –∑–±–µ—Ä—ñ–≥–∞—î–º–æ —è–∫ en
            newDescription.en = {
              title: currentDescription.title || 'Description',
              description: currentDescription.description || '',
            };
          } else if (currentDescription.en) {
            // –í–∂–µ —î –Ω–æ–≤–∏–π —Ñ–æ—Ä–º–∞—Ç - –∑–±–µ—Ä—ñ–≥–∞—î–º–æ —ñ—Å–Ω—É—é—á—ñ –¥–∞–Ω—ñ
            newDescription.en = currentDescription.en || {};
          } else {
            newDescription.en = {};
          }
        } else {
          newDescription.en = {
            title: 'Description',
            description: translation.description || '',
          };
        }

        // –î–æ–¥–∞—î–º–æ —Ä–æ—Å—ñ–π—Å—å–∫—É –≤–µ—Ä—Å—ñ—é
        newDescription.ru = {
          title: '–û–ø–∏—Å–∞–Ω–∏–µ',
          description: translation.description || '',
        };

        // –û–Ω–æ–≤–ª—é—î–º–æ infrastructure –∞–Ω–∞–ª–æ–≥—ñ—á–Ω–æ (—è–∫—â–æ –ø–æ—Ç—Ä—ñ–±–Ω–æ)
        let newInfrastructure: any = null;
        if (currentInfrastructure && typeof currentInfrastructure === 'object') {
          newInfrastructure = {
            en: currentInfrastructure.en || (currentInfrastructure.title || currentInfrastructure.description ? {
              title: currentInfrastructure.title || 'Infrastructure',
              description: currentInfrastructure.description || '',
            } : {}),
            ru: currentInfrastructure.ru || {},
          };
        }

        // –û–Ω–æ–≤–ª—é—î–º–æ —á–µ—Ä–µ–∑ raw query –¥–ª—è –ø—Ä–∞–≤–∏–ª—å–Ω–æ—ó –æ–±—Ä–æ–±–∫–∏ JSONB
        const updateData: any[] = [JSON.stringify(newDescription)];
        let updateClause = 'description = $1::jsonb';

        if (newInfrastructure) {
          updateClause += ', infrastructure = $2::jsonb';
          updateData.push(JSON.stringify(newInfrastructure));
        }

        updateData.push(area.id);
        const paramIndex = updateData.length;

        await queryRunner.query(
          `UPDATE areas SET ${updateClause} WHERE id = $${paramIndex}`,
          updateData
        );

        updatedCount++;
        console.log(`‚úÖ [${updatedCount}/${areas.length}] Updated: ${area.nameEn} ‚Üí ${translation.name}`);
      }

      console.log('\nüìä Translation Summary:');
      console.log(`   ‚úÖ Updated: ${updatedCount} areas`);
      console.log(`   ‚ö†Ô∏è  Skipped: ${skippedCount} areas`);
      
      if (notFound.length > 0 && notFound.length <= 20) {
        console.log('\n‚ö†Ô∏è  Areas without translations:');
        notFound.forEach(name => console.log(`   ‚Ä¢ ${name}`));
      }

    } finally {
      await queryRunner.release();
    }

    await AppDataSource.destroy();
    console.log('\n‚úÖ Translation completed!');
  } catch (error: any) {
    console.error('‚ùå Error:', error.message);
    if (error.stack) {
      console.error(error.stack);
    }
    if (AppDataSource.isInitialized) {
      await AppDataSource.destroy();
    }
    process.exit(1);
  }
}

translateAreaDescriptions();
